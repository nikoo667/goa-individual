სახელი = input("შეიყვანე შენი სახელი: ")
ასაკი = int(input("შეიყვანე შენი ასაკი: "))
პაროლი = input("შეიყვანე პაროლი: ")

if სახელი == "":
    print("სახელი არ უნდა იყოს ცარიელი.")
else:
    if ასაკი < 13:
        print("მომხმარებელი უნდა იყოს 13 წლის ან უფროსი.")
    else:
        if len(პაროლი) < 6:
            print("პაროლი უნდა შეიცავდეს მინიმუმ 6 სიმბოლოს.")
        else:
            print("მონაცემები სწორია. რეგისტრაცია წარმატებულია!")